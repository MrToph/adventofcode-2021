use std::convert::From;

enum Instruction {
    Up(u64),
    Down(u64),
    Forward(u64),
}

impl From<&str> for Instruction {
    fn from(item: &str) -> Self {
        if item.starts_with("forward ") {
            let number = item["forward ".len()..].parse::<u64>().unwrap();
            return Instruction::Forward(number);
        } else if item.starts_with("down ") {
            let number = item["down ".len()..].parse::<u64>().unwrap();
            return Instruction::Down(number);
        } else if item.starts_with("up ") {
            let number = item["up ".len()..].parse::<u64>().unwrap();
            return Instruction::Up(number);
        } else {
            panic!("cannot convert string \"{}\" to Instruction ", item);
        }
    }
}

pub fn run(list: &[&str]) -> Option<u64> {
    let mut depth: u64 = 0;
    let mut forward: u64 = 0;

    list.iter()
        .map(|&s| s.into())
        .for_each(|instr: Instruction| match instr {
            Instruction::Forward(amount) => {
                forward += amount;
            }
            Instruction::Up(amount) => {
                depth = depth.checked_sub(amount).unwrap();
            }
            Instruction::Down(amount) => {
                depth += amount;
            }
        });

    Some(depth * forward)
}

